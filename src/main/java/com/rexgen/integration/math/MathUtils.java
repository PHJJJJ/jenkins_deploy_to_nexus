package com.rexgen.integration.math;

public class MathUtils {

    /**
     * 두 숫자를 더한다
     *
     * @param a 첫 번째 숫자
     * @param b 두 번째 숫자
     * @return 두 숫자의 합
     */
    public static int add(int a, int b) {
        return a + b;
    }

    /**
     * 두 숫자를 곱한다
     *
     * @param a 첫 번째 숫자
     * @param b 두 번째 숫자
     * @return 두 숫자의 곱
     */
    public static int multiply(int a, int b) {
        return a * b;
    }
}
